package com.coder.vo;

public class ClassInfo {
	
    public String pagckdir = "";
    public String className = "";
    public String parentClassName = "";
    public String tableName = "";
    public String dataBase = "";
    public String hbmFile = "";
	public String getPagckdir() {
		return pagckdir;
	}
	public void setPagckdir(String pagckdir) {
		this.pagckdir = pagckdir;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getParentClassName() {
		return parentClassName;
	}
	public void setParentClassName(String parentClassName) {
		this.parentClassName = parentClassName;
	}
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public String getDataBase() {
		return dataBase;
	}
	public void setDataBase(String dataBase) {
		this.dataBase = dataBase;
	}
	public String getHbmFile() {
		return hbmFile;
	}
	public void setHbmFile(String hbmFile) {
		this.hbmFile = hbmFile;
	}
    
    
}
